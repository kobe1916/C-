
//类比1+2+3+4+...+n
//先对阶乘项求解，最后累加
int main()
{
	int i, j;
	int x;
	int ret = 1;
	int n = 0;
	scanf("%d", &x);
	//起始不能为0，否则相乘为0 
	for (i = 1; i <= x; i++)
	{
		ret *= i;
		/*for (j = 1; j <= i; j++)
		{
			ret *= j;
		}*/
		//因为先算阶乘是从1开始的然后依次往上乘，所以依次可以看为1！、2！、3！...，eg.1*1,1*2,1*2*3...    
		//只需从1开始每次乘完后，累加即为1!+2!+3!...

		n += ret;
	}
	printf("x！结果为:%d", n);
	system("pause");
	return 0;
}
